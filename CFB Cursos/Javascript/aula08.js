// Bitwise

let n1 = 10
let n2 = 11

let res = n1 & n2   // and
let res2 = n1 | n2  // or
let res3 = n1 ^ n2  // not

let res4 = n1 << 1 // deslocamento em bit para o lado selecionado (Deslocamento de 1 bit para esquerda dobra o valor)
let res5 = n1 >> 1 // deslocamento em bit para o lado selecionado (Deslocamento de 1 bit para direita divide o valor pela metade)

console.log(res)
console.log(res2)
console.log(res3)
console.log(res4)
console.log(res5)